package ab02;

public class Aufgabe01 {
    public static void main(String[] args) {
        for(int i = 5; i <=8; i++) {
            System.out.println(i+ " ");
        }
        for(int i = 9; i >= 0; i--) {
            System.out.println(i+ " ");
        }
        for(int i = -2; i <= 9; i++) {
            System.out.println(i+ " ");
        }
        for(int i = 1; i <= 9; i+=2) {
            System.out.println(i+ " ");
        }
        for(int i = 15; i >= -3; i-=3) {
            System.out.println(i+ " ");
        }
    }
}
